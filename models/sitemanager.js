import mongoose from "mongoose";

const Schema = mongoose.Schema;

const siteManagerSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  mobileNumber: {
    type: String,
    required: true,
  },
  nic: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const SiteManager = mongoose.model("SiteManager", siteManagerSchema);

export default SiteManager;
